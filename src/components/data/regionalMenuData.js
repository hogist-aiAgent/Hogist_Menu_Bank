// Function to get menu data based on region and sub-option
export const getRegionalMenuData = (selectedRegion, selectedSubOption) => {
  if (!selectedRegion || !selectedSubOption) {
    return null;
  }

  // South Indian menus
  if (selectedRegion === "South Indian") {
    if (selectedSubOption === " Breakfast") {
      return southIndianBreakfastData;
    }
    if (selectedSubOption === " Snacks") {
      return southIndianSnacksData;
    }
    if (selectedSubOption === " Lunch & Dinner") {
      return southIndianLunchDinnerData;
    }
  }

  // North Indian menus
  if (selectedRegion === "North Indian") {
    if (selectedSubOption === " Breakfast") {
      return northIndianBreakfastData;
    }
    if (selectedSubOption === " Snacks") {
      return northIndianSnacksData;
    }
    if (selectedSubOption === " Lunch") {
      return northIndianLunchData;
    }
    if (selectedSubOption === " Dinner") {
      return northIndianDinnerData;
    }
  }

  // Andhra menus
  if (selectedRegion === "Andhra") {
    if (selectedSubOption === " Breakfast") {
      return andhraBreakfastData;
    }
    if (selectedSubOption === " Snacks") {
      return andhraSnacksData;
    }
    if (selectedSubOption === " Lunch") {
      return andhraLunchData;
    }
    if (selectedSubOption === " Dinner") {
      return andhraDinnerData;
    }
  }

  // Kerala menus
  if (selectedRegion === "Kerala") {
    if (selectedSubOption === " Breakfast") {
      return keralaBreakfastData;
    }
    if (selectedSubOption === " Snacks") {
      return keralaSnacksData;
    }
    if (selectedSubOption === " Lunch") {
      return keralaLunchData;
    }
    if (selectedSubOption === " Dinner") {
      return keralaDinnerData;
    }
  }

  return null;
};